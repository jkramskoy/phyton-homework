while(True):
    number1 = int(input("Please enter first number"))
    number2 = int(input("Please enter second number"))
    operation = input("Please enter the arithmetic operation")

    if operation == "+":
        print(number1 + number2)
    elif operation == "-":
        print(number1 - number2)
    elif operation == "*":
        print(number1 * number2)
    elif operation == "/":
        print(number1 / number2)
    else:
        print("error")





















